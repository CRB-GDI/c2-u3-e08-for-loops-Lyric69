namespace E08_for_loops
{
    internal class Program
    {
        static void Main(string[] args)
        {
            int multiple = 8;

            Console.WriteLine("0 x " + multiple + " = " + multiple * 0);
            Console.WriteLine("2 x " + multiple + " = " + multiple * 2);
            Console.WriteLine("4 x " + multiple + " = " + multiple * 4);
            Console.WriteLine("6 x " + multiple + " = " + multiple * 6);
            Console.WriteLine("8 x " + multiple + " = " + multiple * 8);
        }
    }
}